#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int, char))
{
	int		i;
	char	*ptr;
	int		str_length;

	i = 0;
	if (!s || !f)
		return (NULL);
	str_length = ft_strlen(s);
	ptr = malloc(sizeof(char) * (str_length + 1));
	if (!ptr)
		return (NULL);
	while (s[i])
	{
		ptr[i] = f(i, s[i]);
		i++;
	}
	ptr[i] = '\0';
	return (ptr);
}
